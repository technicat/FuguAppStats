beginrow();
foreach ($urls as $name=>$url) {
  begincell("bigicon");
  showicon($name,$url);
  echo("<div class=\"apptitle\">");
  showname($name,$url);
  echo("</div>");
  endcell();
  $col = $col + 1;
  if ($col>$cols) {
    endrow();
    beginrow();
    $col = 1;
  }
}
endrow();
echo("</table>");
}

